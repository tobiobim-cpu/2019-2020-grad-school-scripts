    #To analyze data, core components of analytics are divided into the following:
       #1)Descriptive: it answers the questions of "what has happened based on historical data". It summarizes large datasets to describe outcomes to stakeholders
          #Evaluates data through a process known as EXPLORATORY DATA ANALYSIS (EDA)
          #developing KPIs and Metrics are key to measure track success or failure of key objective and performance in specific industries
          #E.g. Generating reportsto provide a view of an organization's sales and financial data.
        
        #2)Diagnostic: it answers the questions "about why events happened". It uses findings from descriptive analytics to uncover cause of these events
            #a)Identify anomalies in the data. These might be unexpected changes in a metric or a particular market
            #b)Collect data thats related to these anomalies
            #c)Use statistical tecniques to discover relationships and trends that explain these anomalies.
        
        #3)Predictive: it answers the question "about what will happen in the future". Uses historical data to identify trends and determine if they are likely to occur.
            #techniques includes variety of statistical and ML techniques such as NN, decision trees, and regression.

        #4)Presciptive: it answers the questions about "which actions should be taken to achieve a goal or target". Uses insights from predictive analytics.
           #It allows biz to make informed decisions in the face of uncertainty. It relies on ML techniques to find patterns in large datasets
           #By analyzing past decisions and events, orgnizations can estimate the likelihood of different outcomes.

        #5)Cognitive: it draws inferences from existing data & patterns, derive conclusion based on existing knowledge bases and then add these findings back into the 
             #knowledge base for future inferences, a self-learning feedback loop
             #It helps you to learn what might happen if circumstances change and determine how you might handle these situations
             #It depends on ML algos, and will use several NLP concepts to make sense of previously untapped data source such as call center conversation logs & product reviews.

